
# menghitung luas dan keliling persegi dengan method function
def kelilingpersegi(sisi):
    return 4*sisi
def luaspersegi(sisi):
    return sisi*sisi

panjang = int(input("masukan panjang sisi :"))
print("keliling persegi : ", kelilingpersegi(panjang))
print("luas persegi : ", luaspersegi(panjang))





